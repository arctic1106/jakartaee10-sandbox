package com.example;

import jakarta.json.bind.annotation.JsonbCreator;

public class Person {
    private String name;
    private int age = 30;

    private String email = null;

    public Person() {
    }

    @JsonbCreator
    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    @Override
    public String toString() {
        return "Person{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}
